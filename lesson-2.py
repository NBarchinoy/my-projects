a = int(input("Enter the first number:"))
c = input("Enter the action symbol:")
b = int(input("Enter the second number:"))
if c == "+":
    print(a+b)
if c == "-":
    print(a-b)
if c == "*":
    print(a*b)
if c == "/":
    print(a/b)
if c == "**":
    print(a**b)
